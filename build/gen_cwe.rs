use std::collections::BTreeMap;

use crate::escape::escape_rust_string;
use crate::types::{JsonRule, RegistryDetector};

pub fn generate_cwe_catalog_code(rule_map: &BTreeMap<u32, JsonRule>) -> String {
    let mut code = String::new();
    code.push_str("// This file is auto-generated by build.rs from ruleset/golang/chunks/*.json\n");
    code.push_str("// DO NOT EDIT MANUALLY\n\n");
    code.push_str("pub static CWE_CATALOG_GENERATED: &[CweRef] = &[\n");
    for (id, rule) in rule_map {
        let url = format!("https://cwe.mitre.org/data/definitions/{id}.html");
        code.push_str(&format!(
            "    CweRef::new({}, \"{}\", \"{}\"),\n",
            id,
            escape_rust_string(&rule.name),
            url,
        ));
    }
    code.push_str("];\n");
    code
}

pub fn generate_go_metadata_code(
    rule_map: &BTreeMap<u32, JsonRule>,
    supported_ids: &[u32],
) -> String {
    crate::gen_metadata::generate_go_rule_metadata_code(
        "CWE",
        "go_cwe_ref_slice!",
        "GO_CWE_RULE_IDS",
        rule_map,
        supported_ids,
    )
}

pub fn generate_go_registry_code(detectors: &[RegistryDetector]) -> String {
    let mut code = String::new();
    code.push_str(
        "// This file is auto-generated by build.rs from src/lang/go/detectors/cwe/registry.toml\n",
    );
    code.push_str("// DO NOT EDIT MANUALLY\n\n");
    code.push_str("const GO_RULES: &[GoRuleEntry] = &[\n");
    for entry in detectors {
        let id = entry.cwe;
        code.push_str(&format!(
            "    (\"CWE-{id}\", {}, &self::metadata::META_CWE_{id}),\n",
            entry.function
        ));
    }
    code.push_str("];\n");
    code.push_str("const _: () = assert!(!GO_RULES.is_empty());\n");
    code
}
