use std::collections::BTreeMap;

use crate::escape::escape_rust_string;
use crate::types::JsonRule;

/// Shared metadata codegen for numbered rule families (CWE, PERF).
pub fn generate_go_rule_metadata_code(
    prefix: &str,
    ref_macro: &str,
    rule_ids_const: &str,
    rule_map: &BTreeMap<u32, JsonRule>,
    supported_ids: &[u32],
) -> String {
    let mut code = String::new();
    code.push_str("// This file is auto-generated by build.rs from ruleset/golang/chunks/*.json\n");
    code.push_str("// DO NOT EDIT MANUALLY\n\n");

    code.push_str(&format!("pub const {rule_ids_const}: &[&str] = &[\n"));
    for id in supported_ids {
        code.push_str(&format!("    \"{prefix}-{id}\",\n"));
    }
    code.push_str("];\n\n");

    let meta_prefix = prefix.to_ascii_uppercase();
    for id in supported_ids {
        let rule = rule_map
            .get(id)
            .unwrap_or_else(|| panic!("missing JSON metadata for supported rule {prefix}-{id}"));
        code.push_str(&format!(
            "pub(super) const META_{meta_prefix}_{id}: RuleMetadata = emit::rule_meta(\n\
             \x20   \"{prefix}-{id}\",\n\
             \x20   \"{}\",\n\
             \x20   \"{}\",\n\
             \x20   severity_for({id}),\n\
             \x20   {ref_macro}({id}, \"{}\"),\n\
             \x20   fix_for({id}),\n\
             );\n\n",
            escape_rust_string(&rule.name),
            escape_rust_string(&rule.description),
            escape_rust_string(&rule.name),
        ));
    }

    code
}

/// BP metadata codegen (inline severity + `metadata_for` dispatch table).
pub fn generate_go_bp_metadata_code(
    rule_map: &BTreeMap<u32, JsonRule>,
    supported_ids: &[u32],
) -> String {
    let mut code = String::new();
    code.push_str(
        "// This file is auto-generated by build.rs from ruleset/golang/bad-practices.json\n",
    );
    code.push_str("// DO NOT EDIT MANUALLY\n\n");

    for id in supported_ids {
        let rule = rule_map
            .get(id)
            .unwrap_or_else(|| panic!("missing JSON metadata for supported rule BP-{id}"));
        code.push_str(&format!(
            "pub(super) const BP_{id}_META: RuleMetadata = emit::rule_meta(\n\
             \x20   \"BP-{id}\",\n\
             \x20   \"{}\",\n\
             \x20   \"{}\",\n\
             \x20   {},\n\
             \x20   bp_ref_slice!(),\n\
             \x20   fix_for({id}),\n\
             );\n\n",
            escape_rust_string(&rule.name),
            escape_rust_string(&rule.description),
            bp_severity_expr(rule.severity.as_deref().unwrap_or("low")),
        ));
    }

    code.push_str("pub(super) fn metadata_for(rule_id: &str) -> Option<&'static RuleMetadata> {\n");
    code.push_str("    match rule_id {\n");
    for id in supported_ids {
        code.push_str(&format!("        \"BP-{id}\" => Some(&BP_{id}_META),\n"));
    }
    code.push_str("        _ => None,\n");
    code.push_str("    }\n");
    code.push_str("}\n");

    code
}

fn bp_severity_expr(raw: &str) -> &'static str {
    match raw.to_ascii_lowercase().as_str() {
        "critical" => "Severity::Critical",
        "high" => "Severity::High",
        "medium" => "Severity::Medium",
        "info" => "Severity::Info",
        "low" => "Severity::Low",
        other => panic!("unsupported BP severity {other}"),
    }
}
